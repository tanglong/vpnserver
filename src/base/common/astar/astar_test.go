type Map struct {
	info [19][38]int
}

func (this *Map) In(x, y int) bool {
	if x >= 0 && y >= 0 && x <= 18 && y <= 37 {
		return true
	}
	return false
}

func (this *Map) IsWall(x, y, xx, yy int) bool {
	if x+dirs[0][0] <= 18 &&
		y+dirs[2][1] >= 0 &&
		this.info[x+dirs[0][0]][y+dirs[0][1]] == 0 &&
		this.info[x+dirs[2][0]][y+dirs[2][1]] == 0 &&
		xx == x+dirs[1][0] &&
		yy == y+dirs[1][1] {
		return true
	} else if y+dirs[2][1] >= 0 &&
		x+dirs[4][0] >= 0 &&
		this.info[x+dirs[4][0]][y+dirs[4][1]] == 0 &&
		this.info[x+dirs[2][0]][y+dirs[2][1]] == 0 &&
		xx == x+dirs[3][0] &&
		yy == y+dirs[3][1] {
		return true
	} else if x+dirs[4][0] >= 0 &&
		y+dirs[6][1] <= 37 &&
		this.info[x+dirs[4][0]][y+dirs[4][1]] == 0 &&
		this.info[x+dirs[6][0]][y+dirs[6][1]] == 0 &&
		xx == x+dirs[5][0] &&
		yy == y+dirs[5][1] {
		return true
	} else if x+dirs[0][0] <= 18 &&
		y+dirs[6][1] <= 37 &&
		this.info[x+dirs[0][0]][y+dirs[0][1]] == 0 &&
		this.info[x+dirs[6][0]][y+dirs[6][1]] == 0 &&
		xx == x+dirs[7][0] &&
		yy == y+dirs[7][1] {
		return true
	}
	return false
}

func (this *Map) IsBlock(x, y int) bool {
	if this.info[x][y] > 0 {
		return true
	}
	return false
}

